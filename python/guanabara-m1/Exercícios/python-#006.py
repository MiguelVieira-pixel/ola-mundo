#VARIÁVEIS
dobro = 1
triplo = 1
raiz = 1.2

#PROCESSO
num = int(input("Digite um número:"))
dobro = num * 2
triplo = num * 3
raiz = num ** 0.5

#SAIDA
print(f"O dobro de {num} é {dobro}.") #dobro
print(f"O triplo de {num} é {triplo}.") #triplo
print('A raiz quadrada de {} é {:.3f}.'.format(num, raiz)) #raiz quadrada
